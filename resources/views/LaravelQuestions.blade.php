	<h3>1=> MVC</h3>
	

	MVC stands for Model View and Controller. 
	The Model handles data and database logic. 
	The View handles UI and output. The Controller 
	connects both and controls the flow. 
	Laravel follows MVC strictly which helps teams 
	work together without stepping on each other’s 
	code.
	It improves code organization and maintainability.
	</div>
	<br>
	<div>

<h3>2=> What are routes in Laravel?</h3>
	
	
	Routes in Laravel are used to define how the 
	application responds to a specific URL request. 
	They act as a bridge between a URL and the code 
	that handles the request, such as a controller 
	method or a closure. Laravel routes are usually 
	defined in the routes/web.php file for web pages 
	and routes/api.php for APIs. Routes can handle 
	different HTTP methods like GET, POST, PUT, and 
	DELETE, and they can also accept parameters, 
	apply middleware, and be grouped for better 
	organization.
	</div>
	<br>
	<div>
	
<h3>3=> What are migrations in Laravel?</h3>
	
	
	Migrations just like everything else are like 
	version control for your database in Laravel 
	systems. These migrations will allow you to 
	create as well as modify or in some cases also 
	delete tables using PHP code without touching 
	the SQL db. This helps teams stay in sync 
	across environments.
	</div>
	<br>
	<div>
	
<h3>4=> What is the Blade templating engine?</h3>
	
	
	Blade is Laravel’s way of handling views. 
	It keeps things clean and readable without 
	turning HTML into a mess. You can reuse 
	layouts and control logic without writing raw
	PHP everywhere. For frontend-heavy roles 
	connected to backend work.
	</div>
	<br>
	<div>
	
<h3>5=> What is caching in Laravel?</h3>
	
	
	Caching helps reduce repeated work by 
	storing results temporarily. Laravel supports 
	multiple cache drivers. When performance 
	becomes a concern, caching often becomes 
	the first improvement step, and interviewers 
	look for that mindset.
	</div>
	<br>
	<div>
<h3>6=> What is database seeding used for?</h3>
	
	
	Seeding helps create predictable data for 
	testing and development. It saves time 
	and avoids manual entries. Teams often 
	rely on it when onboarding new developers.
	</div>
	<br>
	<div>
<h3>7=> What is the Model in Laravel?</h3>
	
	
	A Model in Laravel represents a database 
	table and allows us to interact with its 
	records using Eloquent ORM. It helps us 
	retrieve, insert, update, and delete data 
	without writing raw SQL queries. It can 
	also define relationships such as one-to-one, 
	one-to-many, and many-to-many.
	</div>
	<br>
	<div>

<h3>8=> What is middleware in Laravel?</h3>

	Middleware acts like a filter between the 
	request and the response. It checks conditions 
	like authentication or role access before 
	allowing a request reaches the controller. 
	This keeps controllers clean and logic reusable. 
	Middleware questions often appear in Laravel 
	interview questions because they show an 
	understanding of the request lifecycle.

